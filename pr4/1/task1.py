# Задача 1: Даны два целых числа A и B (при этом A ≤ B). 
# Выведите все числа от A до B включительно.

# Вводим числа A и B
A = int(input("Введите число A: "))
B = int(input("Введите число B: "))

# Проверяем условие A ≤ B
if A <= B:
    # Выводим все числа от A до B включительно
    for i in range(A, B + 1):
        print(i)
else:
    print("Ошибка: A должно быть меньше или равно B")