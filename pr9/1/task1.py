# Задача 1: Дано натуральные числа a, b. Вычислить остаток от деления a на b.
# Решение с помощью рекурсии.

def remainder_recursive(a: int, b: int) -> int:
    """
    Рекурсивно вычисляет остаток от деления a на b.
    
    Args:
        a: Делимое (натуральное число)
        b: Делитель (натуральное число)
    
    Returns:
        Остаток от деления a на b
    """
    # Базовый случай: если a < b, то остаток равен a
    if a < b:
        return a
    
    # Рекурсивный случай: остаток от деления a на b равен
    # остатку от деления (a - b) на b
    return remainder_recursive(a - b, b)


def main() -> None:
    """Вычисляет остаток от деления a на b с помощью рекурсии."""
    try:
        a = int(input("Введите натуральное число a: "))
        if a <= 0:
            print("Число a должно быть натуральным (положительным).")
            return
        
        b = int(input("Введите натуральное число b: "))
        if b <= 0:
            print("Число b должно быть натуральным (положительным).")
            return
        
        remainder = remainder_recursive(a, b)
        print(f"Остаток от деления {a} на {b} равен {remainder}")
        
    except ValueError:
        print("Ошибка ввода. Пожалуйста, вводите только целые числа.")


if __name__ == "__main__":
    main()

